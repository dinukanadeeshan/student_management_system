    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String name = tfName.getText();
        String address = tfAddress.getText();
        String tel = tfTel.getText();
        int rank = (Integer) spnrDRank.getValue();
        School school = (School) cmbbxScl.getSelectedItem();
        int shy = Integer.parseInt(buttonGroup1.getSelection().getActionCommand());

        Parent mother = null;
        Parent father = null;
        List<Parent> parents = new ArrayList();

        List<Sibiline> sibilines = new ArrayList<>();

        Student student = new Student(rank, name, address, shy, tel, parents, school, sibilines);
        if (chbxMotherLive.isSelected()) {
            Designation designation = (Designation) cmbbxMotherDesig.getSelectedItem();
            mother = new Parent("Mother", designation, student);
            parents.add(mother);
        }
        if (chbxFatherLive.isSelected()) {
            Designation designation = (Designation) cmbbxFatherDesig.getSelectedItem();
            father = new Parent("Father", designation, student);
            parents.add(father);
        }
        for (int i = 0; i < dtm.getRowCount(); i++) {

            String type = (String) dtm.getValueAt(i, 0);
            if (sibilinetypes == null) {
                sibilinetypes = SibilineTypeController.getAllSibilinetypes();
            }
            String desc = (String) dtm.getValueAt(i, 1);
            if (type.contains("Elder Brother")) {
                sibilines.add(new Sibiline(desc, sibilinetypes.get(0), student));
            } else if (type.contains("Elder Sister")) {
                sibilines.add(new Sibiline(desc, sibilinetypes.get(1), student));
            } else if (type.contains("Younger Brother")) {
                sibilines.add(new Sibiline(desc, sibilinetypes.get(2), student));
            } else {
                sibilines.add(new Sibiline(desc, sibilinetypes.get(3), student));
            }
        }
        try {
            System.out.println(school);
            StudentController.addStudent(student);
            
            new Thread() {
                @Override
                public void run() {
                    jLabel12.setText("Saved...");
                    try {
                        Thread.sleep(1500);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(AddStudent.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    jLabel12.setText("");
                }
            }.start();
            clear();
        } catch (DatabaseException e) {
            if (e.getMessage().contains("Duplicate entry")) {
                JOptionPane.showMessageDialog(this, "This Rank is Already registerd...", "", JOptionPane.ERROR_MESSAGE);
            }
        }
        // JOptionPane.showMessageDialog(this, "Saved...");

    }//GEN-LAST:event_btnSaveActionPerformed

