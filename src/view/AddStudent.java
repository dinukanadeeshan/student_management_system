    private void loadSchools() {
        b = false;
        if (allSchools == null) {
            allSchools = SchoolController.getAllSchools();
        }
        cmbbxScl.removeAllItems();
        for (School s : allSchools) {
            cmbbxScl.addItem(s);
        }
        cmbbxScl.addItem("New");
        b = true;
    }

    private void loadDesignations() {
        b = false;
        if (allDesignations == null) {
            allDesignations = DesignationController.getAllDesignations();
        }
        cmbbxFatherDesig.removeAllItems();
        cmbbxMotherDesig.removeAllItems();
        for (Designation d : allDesignations) {
            cmbbxMotherDesig.addItem(d);
            cmbbxFatherDesig.addItem(d);
        }
        cmbbxFatherDesig.addItem("New");
        cmbbxMotherDesig.addItem("New");
        b = true;
    }

